/**
 * Transpile embeddable Nodebox sources (.ts) to dist/ as plain ES modules (.js).
 * TypeScript strict checking remains on app code via root tsconfig.json + editors.
 */

import esbuild from "esbuild";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

function collectTsFiles(dir, { skipDirNames = new Set(), skipFiles = new Set() } = {}) {
  /** @type {string[]} */
  const out = [];
  function walk(abs) {
    let entries;
    try {
      entries = fs.readdirSync(abs, { withFileTypes: true });
    } catch {
      return;
    }
    for (const ent of entries) {
      const full = path.join(abs, ent.name);
      if (ent.isDirectory()) {
        if (skipDirNames.has(ent.name)) continue;
        walk(full);
      } else if (
        ent.name.endsWith(".ts") &&
        !ent.name.endsWith(".d.ts") &&
        !skipFiles.has(ent.name)
      ) {
        out.push(full);
      }
    }
  }
  walk(dir);
  return out;
}

function writeIfChanged(file, content) {
  if (fs.existsSync(file) && fs.readFileSync(file, "utf8") === content) return;
  fs.writeFileSync(file, content);
}

function collectBuiltinToolNames() {
  const dir = path.join(root, "src/agent/runtime/tools/builtins");
  return fs.readdirSync(dir)
    .filter((name) => name.endsWith(".ts") && !name.endsWith(".d.ts") && name !== "index.ts")
    .map((name) => name.slice(0, -3))
    .filter((name) => /^[a-z][a-z0-9_]*$/.test(name))
    .sort();
}

function generateBuiltinIndex() {
  const names = collectBuiltinToolNames();
  const target = path.join(root, "src/agent/runtime/tools/builtins/index.ts");
  const imports = names.map((name, index) => `import tool${index} from "./${name}.js";`).join("\n");
  const entries = names.map((_, index) => `  tool${index},`).join("\n");
  writeIfChanged(target, `${imports}\n\nexport const BUILTIN_TOOL_DEFINITIONS = [\n${entries}\n] as const;\n`);
}

async function generateBrowserToolRegistry() {
  const indexPath = path.join(root, "dist/agent-runtime/tools/builtins/index.js");
  const mod = await import(`${pathToFileURL(indexPath).href}?v=${Date.now()}`);
  const catalog = Object.fromEntries(
    mod.BUILTIN_TOOL_DEFINITIONS.map(({ name, run: _run, ...entry }) => [name, entry])
  );
  const target = path.join(root, "dist/agent-runtime/tools/registry-browser.js");
  writeIfChanged(
    target,
    `// Generated by scripts/build-embed-runtime.mjs. Do not edit by hand.\nexport const BUILTIN_TOOLS = ${JSON.stringify(catalog, null, 2)};\n`
  );
  writeIfChanged(
    path.join(root, "dist/agent-runtime/tools/registry-browser.d.ts"),
    `export const BUILTIN_TOOLS: Record<string, { emoji: string; description: string; inputSchema?: Record<string, unknown>; requiresConfirmation?: boolean; approvalSummary?: string }>;\n`
  );
}

async function buildAgentRuntimeOptions() {
  const runtimeRoot = path.join(root, "src/agent/runtime");
  const entryPoints = collectTsFiles(runtimeRoot, {
    skipDirNames: new Set(["vendor"]),
  });

  return {
    entryPoints,
    outdir: path.join(root, "dist/agent-runtime"),
    outbase: runtimeRoot,
    bundle: false,
    platform: "node",
    format: "esm",
    target: "es2022",
    logLevel: "warning",
    treeShaking: false,
  };
}

async function buildCapabilitiesOptions() {
  const capRoot = path.join(root, "src/capabilities");
  const entryPoints = collectTsFiles(capRoot, {
    skipDirNames: new Set(),
    skipFiles: new Set(["index.ts"]),
  }).filter((p) => {
    const rel = path.relative(capRoot, p).replace(/\\/g, "/");
    return /^(tools\/[^/]+\/handler|channels\/[^/]+\/runtime)\.ts$/.test(rel);
  });

  if (!entryPoints.length) {
    console.warn("build-embed-runtime: no capability handler/runtime .ts files found");
    return null;
  }

  return {
    entryPoints,
    outdir: path.join(root, "dist/capabilities-embed"),
    outbase: capRoot,
    bundle: false,
    platform: "node",
    format: "esm",
    target: "es2022",
    logLevel: "warning",
    treeShaking: false,
  };
}

const watch = process.argv.includes("--watch");

generateBuiltinIndex();
const agentOpts = await buildAgentRuntimeOptions();
const capOpts = await buildCapabilitiesOptions();

if (watch) {
  const ctxs = [await esbuild.context(agentOpts)];
  if (capOpts) ctxs.push(await esbuild.context(capOpts));
  await Promise.all(ctxs.map((c) => c.watch()));
  console.error("[embed-runtime] watching TypeScript embed sources…");
  await new Promise(() => {});
} else {
  await esbuild.build(agentOpts);
  await generateBrowserToolRegistry();
  if (capOpts) await esbuild.build(capOpts);
}
